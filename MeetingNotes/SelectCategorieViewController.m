#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    // deselect old category
    if ([[self.tableView cellForRowAtIndexPath:indexPath ] accessoryType] == UITableViewCellAccessoryCheckmark)
    {
        [[self.tableView cellForRowAtIndexPath:indexPath] setAccessoryType:UITableViewCellAccessoryNone];
        self.selectedCategory = nil;
        [self.selectCategoryViewControllerDelegate didSelectCategory:self.selectedCategory];
        NSLog((@"An category has been de-selected for this meeting"));
    }
    else
    {
        [[self.tableView cellForRowAtIndexPath:indexPath] setAccessoryType:UITableViewCellAccessoryCheckmark];
        [[self.tableView cellForRowAtIndexPath:[fetchedResultsController indexPathForObject:self.selectedCategory]] setAccessoryType:UITableViewCellAccessoryNone];
        self.selectedCategory = nil;
        self.selectedCategory = (Category*)[fetchedResultsController objectAtIndexPath:indexPath];
        [self.selectCategoryViewControllerDelegate didSelectCategory:self.selectedCategory];
        NSLog(@"A category has been selected for this meeting");
    }
}

